    private void jButtonEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEntrarActionPerformed
        PreparedStatement pstLogin;  //Este trecho realiza consulta tabela atendente, verificando se o usuario existe
        try {
            pstLogin = conectaLogin.conn.prepareStatement("SELECT * from single.atendente where  login =? and senha =  md5(?)");
            pstLogin.setString(1, (jTextFieldLogin.getText()));
            pstLogin.setString(2, (jPasswordFieldSenha.getText()));
            pstLogin.execute();
            ResultSet rsLogin = pstLogin.getResultSet();
            if (rsLogin.next()) {
                atendente.setId(rsLogin.getInt("id"));
                atendente.setLogin(rsLogin.getString("login"));
                atendente.setNome(rsLogin.getString("nome"));
                atendente.setSenha(rsLogin.getString("senha"));
                atendente.setPermissao(rsLogin.getString("permissao"));

                FrmPrincipal principal = new FrmPrincipal(atendente);
                //  FrmPrincipal principal = new FrmPrincipal(jTextFieldLogin.getText()); // pega string da consulta envia por paramentro
                principal.setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Login ou senha inv√°lidos!"
                        + "\n Por favor verifique se os campos foram preenchidos corretamente");
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrmLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonEntrarActionPerformed

